public class Questao1 {
    public static void main(String[] args) {

        int indice = 13, soma = 0, k = 0;

        while (k < indice) {
            k++;
            soma = soma + k;
        }
        System.out.println("\nSOMA = " + soma);

        //O valor da variável SOMA deverá ser 91. SOMA = 91

    }
}